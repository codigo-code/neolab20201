package tiposPrimitivos;

public class TiposPrimitivosMain {

    public static void main(String[] args) {

        ///////////////////////
        // Numéricos enteros //
        ///////////////////////

        // byte -> 8 bits (-128 a 128)
        byte byte1 = -128;
        // byte byte1 = 293;

        // short -> 16 bits (-32768 a 32767)
        short short1 = 25607;

        // int -> 32 bits (-2147483648 a 2147483647)
        int int1 = 1034039050;

        // long -> 64 bits (-9223372036854775808 a 9223372036854775807)
        long long1 = 939324124;

        /////////////////////////
        // Numéricos flotantes //
        /////////////////////////

        // float -> 32 bits (+/- 3.40282347E+308 a +/-1.40239846E-45)
        float float1 = 4234234234234234234234.3324342342342342345f;

        // double -> 64 bits (+/-1.79769313486231570E+308 a +/-4.94065645841246544E-324)
        double double1 = 234234234234234234234234.23423423423423423;

        //////////////
        // Unicode  //
        //////////////
        // char -> 16 bits (\u0000 a \uFFFF)
        char char1 = 'x';
        char char2 = '\u1111';

        //////////////
        // Booleano //
        //////////////
        // boolean -> 1 bit (true o false)
        boolean bool1 = true;

    }
}
